package ec.unexus.prueba.servicio.tony.veas.utils;

/**
 * TipoDireccion es una enumeración que define los diferentes tipos de direcciones que un cliente puede tener.
 * Los valores posibles son 'MATRIZ' y 'SUCURSAL'.
 *
 * 'MATRIZ' se refiere a la dirección principal o matriz del cliente, mientras que 'SUCURSAL' 
 * se refiere a las direcciones de las sucursales del cliente.
 **/
public enum TipoDireccion {
	MATRIZ, SUCURSAL
}
